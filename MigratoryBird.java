package birdyRun;

public class MigratoryBird {
	private int xsize;  //birds have size
	private int ysize;
	
	public void setXSize(int x) { //getters and setters
		this.xsize = x;
	}
	public int getXSize() {
		return xsize;
	}
	public void setYSize(int y) { 
		this.ysize = y;
	}
	public int getYSize() {
		return ysize;
	}
}
